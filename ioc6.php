<h2>Un Desastre Arqueológico</h2>

<p>La tormenta prontó condenó a los habitantes de la estación a una forzada rutina. Los príncipes y los aprendices se dedicaron a sus tareas durante el día y a planear toda suerte de chifladuras defensivas por la tarde que, de absurdas, nunca llegaban a fructificar en nada. El teniente se enfrascraba en el papeleo y en preparar los exámenes de los aprendices. Solo las cocineras mantenían casi intactas sus tareas habituales. En cuanto a los hombres de ciencia y Palwyr, éstos se habían encerrado en su taller estudiando, clasificando muestras y redactando informes para el museo.</p>

<p>El amanecer en el que terminó la tormenta llegó a hurtadillas. Yei fue la primera en darse cuenta. Después, poco a poco, todos los residentes notaron primero el silencio y, después, la vuelta a los otrora familiares rumores de fondo: el diálogo de los pájaros, el arruyo del mar y la brisa que acariciaba la casa. Durante un minuto todos callaron en dulce escucha. <q>Hip, hip, ¡hurra!, &mdash;gritó una voz juvenil, a la que siguió todo un coro de alegrías, de música de piano y de bailes. Un cielo entreverado de nubes tras las que saludaba un sol victorioso eran ocasión de fiesta que culminó con el descorchar de un viejo champán.</p>

<p>La fiesta, a decir verdad, no duró una hora. El alba nunca es tiempo para celebraciones y mucho menos cuando todos intuían que habría mucho trabajo que hacer fuera; como así fue. Las cocineras encontraron su huerto destruído; la cosecha perdida en el sentido literal de la palabra, esto es no había rastro de las plantas que tenían creciendo y de la tierra, la mitad se había perdido, arrastrada quizás hasta el mar. Imi y Zaq se preparaban para relevar al comodoro y la vigía en la torre de observación; lo que les fastidió un poco, por una parte se separaban de los príncipes, pero por otra podrían vigilar el mar personalmente. A decir verdad, la única parte trágica, se reservaba para los arqueólogos.</p>

<p>El cobertizo que protegía la excavación había volado. Ninguno de sus muros había desaparecido. Huelga decir que lo mismo había pasado a su contenido. La excavación estaba inundada, llena de barro y basura, y seguramente los valiosos restos de los primeros estratos habían desaparecido.</p>

<p>El Doctor Zenón se había unido en triste asamblea al Doctor Tad y su hijo Palwyr contemplando el destrozo que había hecho la tormenta. Palwyr acuclillado, como los niños pequeños, trataba patéticamente de descubrir algo que hubiera sobrevivido; los adultos sencillamente habían perdido toda esperanza.</p>

<p class="d">Tendremos que empezar de nuevo; buscar otra excavación, ir de vuelta a los libros.</p>

<p class="d">Por favor acepte mis condolencias, Doctor Tad. &mdash;La tristeza del Doctor Zenón iba más allá de la pérdida científica que había traído ese desastre porque <q>de vuelta a los libros</q> significaba volver al museo, a un estudio que podría durar meses o años.</p>

<p>Palwyr también lo sabía y aunque parte de él quería correr a la vida civilizada, mayor era su sed de la aventura que hasta ahora había estado viviendo. &mdash;Padre, creo que no se ha perdido todo; todavía tenemos una oportunidad.</p>

<p class="d">Palwyr, hemos perdido el estrato, el estrato entero. Aunque encontremos algo ahí debajo, ¿me dices cómo lo datamos?</p>

<p class="d">Quizás por el estudio de la cerámica, &mdash;Palwyr se dió cuenta inmediatamente de que había cometido un error, &mdash;o…</p>

<p class="d">O nada, esta cultura estaba demasiado aislada para hacer un estudio comparativo, lo sabes perfectamente.</p>

<p class="d">Padre, creo que estoy viendo algo, y esta vez no es pequeño.</p>

<h3>Os Mintieron</h3>

<p>El brujo temblaba de miedo en la cueva de la Señora de las Sombras, sentado en el frío suelo, sin más compañía que un libro grabado en decenas de fémures de animales sacrficados. Nada de la sabiduría ancestral, ni de la magia más oscura le había revelado ninguna manera de traer de vuelta a los príncipes. De hecho, de acuerdo al conocimiento antiguo, lo que le habían revelado sus visiones era falso. Si así fuera, Drugtri acabaría por contentar a la Señora de las Sombras con sus propias vísceras: era hombre muerto y, aún peor, maldito para la vida postrera. Todos tenemos que morir, pero no desnudos y atados, eviscerados vivos para luego agonizar entre las cucarachas de una cueva.</p>

