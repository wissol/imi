<h3>En la Torre de Observación</h3>

<p>Zaq servía ahora en la plataforma que coronaba el exterior de la torre; vigilando el horizonte con sus prismáticos, separado de una caida inmensa por el fino cable del guardamancebos. El mar, tan limpio como siempre, le resultaba ahora amenazante, detrás de cada ola podría ocultarse un asesino que él no permitiría acercarse; no mientras estuviera de guardia. Todo lo que vió fueron los sospechosos habituales, la familia de orcas que acechaba las focas y las nutrias de los islotes y que habían vuelto tan pronto como había acabado la tormenta. Zaq sabía que las proezas gimnástica que rompían el agua en espuma significaban la muerte de alguna criatura. Pronto se sintió identificado con las víctimas; también él tenían que estar atento a un asesino mucho más fuerte.</p>

<p>Media hora después las orcas se habían marchado, seguramente repletas de carne, y las focas habían vuelto al mar, espoleadas por el hambre de peces. Zaq ya sentía en sus huesos el frío que poco a poco había penetrado su uniforme y apuraba los últimos momentos hasta volver al interior de la torre cuando una columna de humo llamó su atención: había avistado un vapor y era de los rápidos.</p>

<p>Contuvo sus deseos de volver corriendo a la torre y levantar de sus siestas a Imi y al teniente, hasta tener seguridad de qué clase de barco se trataba. Al principio distinguió velas blancas: se trataba de un motovelero, después un castillo de popa que le resultó muy familiar, finalmente distinguió el gallardete dorado de la Mayordomía de Guardacostas. Ahora sí que iba a correr.</p>

<p class="d">¿Cómo? ¿Estás seguro? &mdash;preguntó el teniente.</p>

<p class="d">Sí, lo he visto muy claramente.</p>

<p class="d">Debe ser el barco de suministros; deben haberlo adelantado a causa de la tormenta. Querrán comprobar que todo está en orden. Bueno, pues mejor, tendremos fiesta. &mdash;El teniente calló un momento. &mdash;Los náufragos se irán con ellos.</p>

<p class="d">Ya… es verdad. &mdash;Zaq había tenido un amigo, un vendedor de periódicos, que vivía en una iglesia en ruinas, muy feliz tras haberse escapado de uno de esos orfanatos que ahora esperaban a los príncipes.</p>

