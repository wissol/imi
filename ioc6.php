<h3>El Insomnio del Ursurpador</h3>

<p>Drugtri paseaba delante del trono, su puño derecho, cerrado como una piedra, goteaba sangre. Imaginaba discursos ante una audiencia imaginaria, que probaría el asesinato de los príncipes y la ejecución de los bandidos. Pero mientras de los segundos tenía sus cabezas conservadas en formol, de los primeros no tenía noticia. Seis veces había hecho registrar el pozo, ajusticiando a un <em>bandido</em> cada vez y cinco veces había sacado agua y una vez una rata muerta.</p>

<p>Abrió la mano, sobre sus dedos, otro recién amputado le ofrecía cierta seguridad de que pronto tendría los medios para cazar a los príncipes.</p>

<p>Los nueve dedos del brujo sostenía una escudilla de la <q>Poción de Alivio</q>que acababa de tomar. El fresco muñón de su meñique izquierdo, cosido con nueve puntos todavía palpitaba, aunque había dejado de doler. El brebaje contenía elementos de estrellas caídas, pero era necesario. Tenía que encontrar pronto un camino, una forma de ir hacia los príncipes o traérselos a él y esa era una magia de la que no sabía ni su existencia. ¿Pero dónde investigaría? Quizás si preguntara a los muertos…</p>

