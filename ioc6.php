<p>Drugtri suspiró. Ahora parecía que podía confiar en la visión del brujo. Quizás con un sacrificio mayor encontrara a decirle la dirección a dónde habían partido los niños. Si no, bueno, todavía le quedaban nueve dedos que cortar.</p>

<h3>El Hallazgo</h3>

<p>Palwyr, hundido hasta la rodillas en el barro, sostenía radiante un aro de bronze tan ancho como un brazo. Una sonrisa más tímida se había dibujado en los rostros de los adultos. Desde luego, aunque no se pudiera precisar el estrato al que pertencía, aquello era "algo". En seguida empezaron a bullir las especulaciones. Para los arreos de una cabalgadura era demasiado ancho y grueso, para uso en un taller estaba demasiado bien hecho.</p> 

<p class="d">No, su función debía ser decorativa. &mdash;concluyó el Doctor Tad.&mdash;quizás la aldaba de una puerta.</p>

<p class="d">Tendría que ser una puerta enorme, &mdash;apostilló su hijo.</p>

<p class="d">Disculpe mi atrevimiento, Doctor Tad, no es mi campo, ¿pero no le parece que podría ser la entrada a un palacio?</p>

<p class="d">O un templo…</p>

<p>El hallazgo lo cambiaba todo; si estaban ante un palacio o un templo o una estructura semejante, lo que habían encontrado hasta entonces no eran sino baratijas y recuerdos de bazar. Ahora podrían estar ante auténticos tesoros de piedras preciosas y oro, tumbas reales y quién sabe incluso si la posibilidad de reconstruir un mundo perdido.</p>

<p class="d">Padre, ¿seguimos cavando?</p>

