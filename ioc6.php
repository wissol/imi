<p class="d">Os mintieron Majestad, &mdash;así se dirigía a Drugtri, de pie en la desierta sala del trono, un sujeto delgado y atlético, bigotes afilados y ropas de tela. &mdash;Nunca podréis encontrar a los príncipes en el pozo, porque al pozo no fueron nunca.</p>

<p class="d">¿Cómo lo sabes?</p>

