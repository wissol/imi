<p>La primera clase práctica fue, efectivamente, de limpiar y ordenar pero también tuvieron ocasión de ver un relé por dentro y comprobar que, a fin de cuentas, tampoco era una cosa tan complicada. En esencia no era más que un interruptor o un pulsador, como los del telégrafo. La diferencia residía en que el dedo humano era sustituido por la energía del magnetismo. Sólo había que saber dar las órdenes a la máquina para que ésta regulara las corrientes eléctricas como quien pone un madero en una acequia para desviar el agua. Tan pronto como se emitían las instrucciones y los datos los cálculos salían solos.</p>

