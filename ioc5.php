<p>Muy pronto el día se oscureció, nube sobre nube, con una brisa lenta, reposada, mientras el frío iba descendiendo desde las cimas de la isla. El mar se retiraba de la orilla, mientras los frailecillos se hundían en sus refugios de rocas. En el interior, las chimeneas y las estufas recibían generosos alimentos de combustible y se distribuían los quinqués.</p>

<p>Salvo todo esto se hubiera dicho que para los aprendices fue otro día de tareas, si no fuera porque la tarea que emprendieron les sobrecogió. El teniente les condujo al sótano, y a través de él, por un pasadizo, a una extraña puerta que hasta entonces les había sido ocultada.</p>

<p class="d">Ayudadme a quitar esos bultos, &mdash;el teniente señaló una torre de cajas que tapizaba una de las paredes.</p>

<p>El desánimo les duró a los chicos lo justo de coger la primera caja. Polvo y aire, apenas nada más había en su interior, y detrás de ellos una puerta de acero, cuajada de gruesos remaches y cerrada con un volante; como la compuerta de uno de los nuevos barcos de vapor.  Justo entonces el teniente dibujó en su rostro una sonrisa de complicidad. &mdash;Bienvenidos a la cámara de la máquina.</p>

<p>El teniente tatareó una marcha militar, tratando de remedar el clímax de una ópera Germánica, pero todo lo que consiguió fue dar un traspiés cómico a la sobrecogedera imágenes que fueron revelando, según se encendía, una tras otras, las lámparas de gas.</p>

<p class="d">Se, ¿se han encendido solas?</p>

