<h2>Capítulo 5º Sin Mar a la Vista</h2>

<p>La rutina les sorprendió tres días después. Los príncipes habían quedado, de hecho, en manos de la cocinera, quien les buscó ocupación en el huerto y en las miles de pequeñas atenciones que necesitaba la estación. Quedará siempre por saberse si entendieron algo hasta que se encontraron con las tareas, estaban demasiado asustados para protestar o sencillamente no tenían fuerzas para hacerlo en un idioma desconocido. Fuera como fuese, se sentían inmensamente felices de tener el cielo sobre sus cabezas y solo las tormentas, sus obligaciones y la noche los hacían regresar al interior. Salvo por su falta de familiaridad se diría que habían nacido para las tareas del campo.</p>

<p>El jefe de la estación imaginó que los <em>naúfragos</em> tendrían que quedarse allí al menos hasta las vacaciones de los aprendices. En el primer barco de suministros que llegara, daría parte mediante oficio a la Maestría. Con un poco de suerte, para cuando llegara la respuesta, se manejarían lo suficientemente bien como para unirse a los guardiamarinas y evitar el orfanato.</p> 

<p>Pero después de todos esos preparativos la vida volvió a su cauce habitual. Para Imi y Zaq eso siginificó entre otras cosas volver a la teoría del álgebra del señor Boole, el <q>lenguaje de las máquinas</q> y las tareas de observación en la torre o de búsqueda de diminutos artefactos en la excavación arqueológica. <q>Parecemos ardillas</q>, comentó una vez Zaq, <q>siempre buscando, siempre buscando</q>.</p>

<p>Las tardes y las horas de asueto se animaban ahora con las andanzas de los príncipes, las cartas, el ajedrez y hasta las carrera y los juegos de pelota cuando el sol calentaba lo suficiente como para animarse a jugar. Fueron tres semanas de alegría que terminaron con una rara reunión de todo el personal, excepto los príncipes, en el despacho del comodoro.</p>

<h3>La tormenta de nieblas</h3>

<p>Sobre la mesa del comodoro, fijada por libros, un tintero y una maceta, un trozo de un rollo de un papel, largo como una sábana, impreso con una serie de rayas y puntos y titulado a mano con la frase <q>Previsión Meteorológica #345-72a</q>. La cara del Comodoro y del Teniente decían de todo menos sol y tiempo despejado.</p>

<p class="d">Gracias, &mdash;comenzó el comodoro, &mdash;la situación es grave. Tenemos una previsión que indica un año de lluvias fuertes y seguramente inundaciones por todo el Reino. En su momento transmitiremos la información, junto con todo lo demás. En fin… como saben el clima que sufrimos hoy en esta isla es una versión intensificada, agrandada, del que existirá en el Reino en el plazo de un año. Esa es la razón original de la Príncipe Alberto. En fin… tenemos una tormenta buena a las puertas. Los datos hablan de lluvias fuertes durante una semana al menos, de tormenta y de nieblas, espesas y constantes, todo prácticamente a la vez y en un grado que me hubiera parecido imposible. Queda prohibida toda actividad exterior que no tenga mi permiso explícito. Inmediatamente todos nos aprestaremos a preparar la estación para la acometida del clima. De ninguna manera nadie trabajará solo durante este periodo; razón por la cual yo marcharé ahora mismo a la torre. Desde ahora quedamos todos sometidos a la más estricta de las disciplinas. Nos puede ir la vida en ello.</p>

<p class="d">Señor, &mdash;preguntó el Doctor Tad, &mdash;¿hay tiempo para impermeabilizar la excavación?</p>

