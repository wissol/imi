<h2>Capítulo 9 - La Espada y la Sangre</h2>

<p>La espada y la sangre habían caído sobre el reino. Las fuerzas del conde, incrementadas por una milicia de ciudadanos habían espantado a los partidiarios de Drugtri en decenas de pequeñas escaramuzas. Todavía no se había librado una verdadera batalla, pero ya no había vuelta atrás. La victoria o la muerte lenta, dolorosa, humillante y cruel eran las únicas alternativas para los rebeldes. <q>¿Dónde están los príncipes?</q> se había convertido en su grito de guerra, la esperanza del pueblo y la preocupación de los líderes.</p>

<p> En el gran salón de una casa solariega donde el ejército rebelde había establecido su centro de mando para esa noche, se reunía el conde con los comandantes de su guardia y el filósofo.</p>

<p class="d">Los príncipes, esa es la clave de todo, ¿dónde están los príncipes? &mdash;iniciaba así su intervención el conde. &mdash;Yo no puedo ser el rey, ni deseo serlo, pero si no encontramos los príncipes, tenemos un problema. &mdash;El conde se encontró con miradas de incomprensión, pero nadie se atrevió a interrumpirle. &mdash;Todavía no somos suficientemente fuertes, necesitaremos aliados y las primeras victorias nos los traerán, pero de esos aliados algunos querrán reinar. Necesitamos encontrar a los príncipes.</p>

<p class="d">Usía, temo que los príncipes estén en un pozo.</p>

<p class="d">Y yo también, centurión, y yo también. Pero mientras los estemos buscando, mientras mantegamos la esperanza el pueblo no aceptará a ningún pretendiente. Con suerte podremos mantener la charada hasta que la cabeza de Drugtri cuelgue de una pica.</p>

<p class="d">¿Y después?</p>

<p class="d">Después nos concentraremos en evitar otra guerra civil. Pero de momento, ¿qué diremos? ¿qué haremos?</p>

<p class="d">Usía, &mdash;habló entonces el filósofo,&mdash; anoche me visitó un ángel.</p>

<p>El silencio interrumpió los pensamientos de todos mientras la palabra <q>loco</q> se formaba en todas las mentes. &mdash;Usía quiero decir que dejaremos correr el rumor de que anoche me visitó un ángel, digamos que en sueños, y ese ángel me dijo que los príncipes seguían vivos, pero escondidos, muy lejos de aquí y que cuatro caballeros <q>de corazón puro</q>, esto es jóvenes e inexpertos, los que consideréis menos duchos para la batalla pero que no parezcan tontos, me acompañarán en esa búsqueda, <q>lejos, más allá de las montañas</q>. Oficialmente, claro, iremos a buscar reconocimiento de los monarcas extranjeros, pero dejaremos correr el rumor. Al fin y al cabo, ¿no existe ya el rumor de qué Drugtri también los están buscando?</p>

<p class="d">Sea pues. Dadme tiempo; será una guerra difícil tal como están las cosas, lo menos que podemos permitirnos es luchar entre nosotros.</p>

<h3>El Espejo</h3>

<p class="d">¡Es un tesoro! ¡Uno de verdad! ¡Venid a ver! ¡Somos ricos! ¡Todos ricos somos!</p>

<p>Imi y Zaq se precipitaron al interior de la cámara levantando una nube de polvo y excitación. &mdash;Sagradas Calles de la Ciudad Santa, ¿qué es esto?</p>

<p class="d">Un tesoro, un tesoro</p>

<p>Se hizo el silencio de nuevo durante cinco aprensivos segundos. Zaq y Arti mantenían una sonrisa triunfal. Imi se acercó a examinar las piezas más detenidamente. &mdash;Son de latón. No es oro.</p>

<p class="d">¿Y la plata?</p>

<p class="d">Es pintura o algo, se me deshace en las manos y debajo hay madera. Lo siento.</p>

<p>El silencio ahora fue desolador y duró hasta que Zaq expresó una esperanza resignada &mdash;Estoy seguro que el doctor pensará que es un gran tesoro arqueológico.</p>

<p class="d">Lo es, &mdash;se esforzó en apoyar Imi, &mdash;seguro que lo es.</p>

<p class="d">Bueno, ¿volvemos arriba?</p>

<p class="d">No, primero hay que aprender muy bien todo lo que hemos encontrado.</p>

<p>Fueron así examinando todos los elementos de la cámara, sin que ninguno, a pesar de sus inscripciones o su valor histórico, les devolviera la excitación perdida, hasta que Arti descubrió una ranura en la pared.</p>

<p class="d">Eh, mirad.</p>

<p>Arti se apoyó en la pared, y está cedió bajo su peso. Se trataba de una puerta disimulada, apenas visible en la oscuridad, que le llevó a un espectáculo magnífico. Ante él se revelaba una cámara esférica, de paredes pintadas en rojo, ocre y negro, los colores del paleolítico, pero apenas lo suficientemente alta como para que él pudiera permanecer de pie. A la derecha, ante una hornacina que resguardaba el ídolo de una diosa-madre, descansaba una altar de piedra con decoraciones de lapislázuli. Bajo el altar, cuatro ánforas, dos de ellas vacías, pero las otras llenas de figuritas que representaban niños, ancianos, mujeres embarazadas, brazos y piernas, de muchos materiales distintos, la mayoría de cerámica, pero también muchas doradas.</p>

<p class="d">Es latón, ¿<q>veredad</q>, Imi?</p>

<p class="d">No, no es latón. A lo mejor es oro. En serio.</p>

<p class="d">No toquéis nada. &mdash;les advirtió Zaq.</p>

<p class="d">¿Por qué? ¿Tienes miedo al ídolo? ¿Nos va a maldecir o algo?</p>

<p class="d">No tengo miedo a los ídolos, tengo miedo a los doctores, &mdash;Las palabras de Zaq no sonaban muy convincentes. &mdash;recordad que tenemos que examinarlo todo, no sacar las cosas. Hay que mirar el otro lado.</p>

<p>Al otro lado, justamente en frente del altar, en otra ornacina había un sencillo espejo circular de metal pulido, apenas mayor que un plato de postre. &mdash;No es de plata, ¿<q>veredad</q>-verdad, Imi?</p>

<p class="d">No lo cre…</p>

<p>La respuesta de Imi quedó cortada cuando sobre la superficie del espejo se formó la imagen, más nítida que en el nuevo invento de feria, el cinematógrafo, de una majestuosa ciudad amurallada.</p>

<p class="d">Es mi reino, allí yo era príncipe.</p>

<h3>Despedida a los Héroes</h3>

<p>Las montañas que rodeaban Valgravia atardecían engalanadas con rosas pasteles que un mortecino sol emitía sobre ellas. El silencio de la naturaleza alejaba infinitamente el lugar de los valles donde dos ejércitos maniobraban y combatían en busca de una posición de ventaja. Las figuras de cinco jinetes se recortaban contra el horizonte que las cumbres habían grabado en el cielo. Cuatro adolescentes revestidos de acero abrían la marcha que cerraba el filósofo. Ascendían lentamente, dando tiempo a los caballos a recuperarse del esfuerzo de la jornada.</p>

<p class="d">Excelencia, &mdash;uno de los jóvenes se dirigió al filósofo&mdash; no veo el refugio todavía.</p>

<p class="d">Es esa cabaña</p>

<p>Los ojos del joven noble no podían creer lo que sus oídos le transmitían. &mdash;¿Eso es una choza de pastor?</p>

<p class="d">Ciertamente; una choza de pastor que levantó mi padre. Me salvó la vida un par de veces siendo niño y quizás los agentes de Drugtri la conozcan menos que las posadas del Camino Real. Además es más barata.</p>

<hr>

<p>La choza era apenas lo suficientemente grande para alojar a los cinco hombres, además de sus monturas. Las paredes eran de troncos, el suelo de tierra y no disponía de más comodidades que un pozo en sus alrededores y un hogar donde calentarse y cocinar. Se la encontraron vacía, como era costumbre en esa época del año, por lo que tuvieron que dedicarse a preparar sus propios lechos; mientras uno de ellos montaba guardia. Nadie tuvo muchas ganas de hablar hasta que, caído ya el sol, envueltos en mantas y arropados por el fuego de la chimenea, el mismo joven que se había sorprendido por la choza volvió a preguntar al filósofo.</p>

<p class="d">Excelencia, me gustaría hacerle una pregunta personal, quizás le parezca un poco rara, pero he escuchado un rumor.</p>

<p class="d">¿Un rumor?</p>

<p class="d">Sí excelencia… bueno, es algo de los príncipes y… bueno, de un ángel. ¿Sabe algo de eso?</p>

<h3>Persecución</h3>

<p>La comitiva del filósofo no llegó a ver el alba en la cabaña. Serían las tres cuando los lobos del aire &mdash;una especie de grandes búhos de montaña que cazan en grupo&mdash; perturbaron su sueño a chillidos de alarma.</p>

<p class="d">¡Vamos! ¡Despertad! Alguien se acerca.</p>

<p class="d">Excelencia, ¿salgo a ver quién es?</p>

<p class="d">¿Quién es? ¿Subiendo la montaña de noche? O un criminal que teme por su vida o algún esbirro de Drugtri que busca nuestro cuello.</p>

<p>A trompicones, apenas apañándoselas en la oscuridad los jóvenes caballeros terminaron de vestirse y recogieron sus armas. Afuera, de pie junto a su caballo, vigilante, les esperaba ya el filósofo. Los cascos de caballos desconocidos retumbaban más allá de la oscuridad. Sin una palabra el grupo emprendió la huida, hincando espuelas, haciendo a sus caballos acelerar progresivamente hasta llegar casi al galope. Cerca, demasiado cerca, bajo la luz de la Luna Azul, se recortaban las figuras de sus perseguidores; apenas más que sombras, solo de vez en cuando se adivinaba el brillo de algún acero. La cabalgada continuó con la furia del odio y de la lucha por la supervivencia. Poco a poco los agentes de Drugtri iban limando la ventaja de los inexpertos caballeros hasta que se hizo evidente que la confrontación se haría insoportable.</p>

<p>Entonces, sin en el menor aviso, uno de los jóvenes frenó a su caballo, dió la vuelta y cargó contra el enemigo. En su primer ataque no hirió a nadie, pero tampoco espada alguna perforó su carne. En la segunda carga, su lanza se rompió dentro del caballo de un enemigo; jinete y bestia se precipitaron al suelo bajo su ardor juvenil. Después le rodearon siete espadas que asediaron su escudo, haciéndole retroceder, rodeándole, hasta que su espalda se quebró y quedó agonizante colgando de los estribos.</p>

<p>En ese momento, el filósofo y sus acompañantes eran ya un rastro lejano.</p>

<h3>Un Niño Enfermo</h3>

<p>Arti, Imi y Zaq emergieron del túnel cubiertos del polvo de los escombros y las manos vacías.</p>

<p class="d">Muy bien, &mdash;les preguntó el Doctor Tad.&mdash; ¿Qué habéis encontrado?</p>

<p>Imi le fue relatando detalle por detalle todo lo que habían examinado hasta llegar al momento de hablar del espejo. &mdash;Perdone, ya sé que nos dijo que no tocáramos nada, pero lo que traigo es excepcional y… esto… si no lo ve seguro que no lo cree. &mdash;Y dicho esto abrío su camisa y extrajo de ella el espejo.</p>

<p class="d">Ah un espejo, pero eso no es tan raros en culturas antiguas y… &mdash;el arquéologo no podía creer lo que estaba viendo; era él mismo, un poco más viejo, recibiendo los aplauso de la gran sala Real Academia de Ciencias. &mdash;Palwyr pasó del enfado contenido que traía al asombro de verse a él mismo dando clases en la universidad mientras el Doctor Zenón descubrió una extraña cueva y una extraña figura que no podía reconocer; pero que era la del brujo.</p>

<p>Todos se quedaron estupefactos durante un rato hasta que el Doctor Zenón les anunció asustado: &mdash;Rápido, vámonos de aquí, rápido, estamos teniendo alucinaciones. Debe ser el aire del túnel.</p>

<hr>

<p>Al final de esa tarde, siguiendo los temores del doctor Tad, Arti se sintió muy cansado. La cocinera Yei, le palpó la frente: apenas tenía fiebre pero aún así lo mandó a la cama antes de terminar el servicio de la cena en consideración a sus esfuerzos como explorador.</p>

<p class="d">Pero Yei, si ya queda poco.</p>

<p class="d">Razón de más. No estás dando pie con bola, y ahora mismo molestas más que ayuda. Hala, a dormir y mañana ya vas preparando el desayuno en cuanto te levantes, ¿estamos?</p>

<p>Arti obedeció suspirando y arrastrando los pies descalzos, la cabeza gacha y un extraño rumor en la cabeza, como si escuchara moscas. Le llegaron las fuerzas para alcanzar su cama y tumbarse en ella, de lado a lado, todavía vestido y las piernas colgando a la altura de las rodillas.</p>

<p>Imi y Zaq lo encontraron al volver de comer. &mdash;Imi, ayúdame a ponerlo bien. &mdash;El príncipe tenía la piel caliente, enrojecida, cubierta de sudor que exhalaba un olor ácido. Alrededor de la boca la colcha mostraba manchas de babas y mocos, y las comisuras de los labios aparecían cuarteadas.</p>

<p class="d">Zaq, límpialo un poco, voy a llamar al comodoro</p>

<p>Quince minutos más tarde la estación estaba movilizada. Arti estaba ahora propiamente arropado, la cara limpia y con Yei a su lado, limpiándole las babas. El Comodoro estaba sentado en la cama que normalmente compartían los aprendices buscando información en el <q>Vademecúm Médico para Uso en Estaciones Remotas</q>. Imi y Zaq esperaban de pie. Las hijas de la cocinera se afanaba preparar seis infusiones de seis hierbas distintas, <q>para según lo que diga el libro.</q> Mientras tanto el doctor Tad conversaba con el doctor Zenón en torno a una botella de cognac.</p>

<p class="d">Será bueno preguntar luego al comodoro los detalles de esa infección. Quizás esa enfermedad sea en sí misma un resto arqueológico.</p>

<p class="d">Quizás sí, doctor Zenón, aunque no creo que nos ayude mucho en ningún caso. Verá, las fuentes arqueológicas no suelen ser muy específicas en cuanto a las enfermedades: o es una peste, o fiebres o simplemente el dios tal o cual hirió al rey. De todas maneras es excitante que algo vivo quede de tiempos tan remotos; aunque pobre niño. Doy gracias al cielo de que no es Palwyr.</p>

<p class="d">Al cielo y a su prudencia, querido colega. Tenía usted razón.</p>

<p>Como el comodoro no pudo encontrar la enfermedad que padecía Arti en el manual optó por la regla general que aparecía en el libro: tratar los síntomas uno por uno aliviando los padecimientos del muchacho. Afortunadamente solo la fiebre resulta preocupante y para eso la infusión de la corteza del árbol del rebedul que Yei conocía muy bien podía ser suficiente. La noche la pasaron turnándose en cuidar al enfermo, vigilándolo y limpiando las babas según aparecía. Por lo demás Arti pasaba casi todo el tiempo durmiendo o en un estado de sopor tal que era incapaz de entender qué le estaba pasando.</p>

<h3>Tiempo y Espacio</h3>

<p>Imi cuando terminó su turno se fue directamente a la sala de la máquina de Boole. Compartía habitación con Arti y tanta entrada y salida no le dejaba dormir. Además, se acercaba ya el alba y pensó que para cuando cogiera el sueño le iba a tocar levantarse. Por lo menos aquí iba a tener algo que hacer y vigilar los datos, por rutinarios que fueran, le permitía distraer la mente de pensamientos tristes. También su madre había muerto de forma parecida.</p>

<p>Pero lo que encontró no tenía sentido en absoluto. El test de diagnóstico no reveló problema alguno e Imi se enfrascó con el manual de interpretación de datos perdiendo todo el sentido del tiempo hasta que Zaq tuvo que traérselo al desayuno.</p>

<p class="d">¿Cómo está Arti?</p>

<p class="d">Igual. Yei le va a hacer sopa.</p>

<p class="d">Sopa no, Zaq, un caldito, que podría atragantarse; pero le he puesto de toda sustancia.</p>

<p>Zaq se fijó entonces en que los ojos de la cocinera mostraban toda una noche de trabajo y preocupaciones. &mdash;Yei, ¿no ha dormido usted?</p>

<p class="d">Bueno, yo ya recuperaré el sueño. ¿Y tú has dormido bien?</p>

<p class="d">Más o menos, pero Imi necesita café.</p>

<p class="d">Sí, un poco de café en la leche no le irá mal. Imi, querido, ¿qué has estado haciendo?</p>

<p class="d">Es la máquina. La mayoría de los datos de temperatura, el viento y todo eso de metereología están bien, pero hay unos datos raros que no tienen sentido y ya no sé que hacer.</p>

<p class="d">A lo mejor es está estropeada</p>

<p class="d">No. Ya lo comprobé. A lo mejor es que soy un niño y no entiendo nada.</p>

<p class="d">O a lo mejor es que tienes que pensar un poco más. ¿Sabes? Si siempre estás diciendo que eres demasiado joven para hacer algo pronto dirás que eres demasiado viejo.</p>

<hr>

<p>Imi volvió a la máquina de Boole todavía con el cuerno de un croissant en la boca. Tenía el día libre y el propósito de desvelarse a sí mismo hasta el último secreto de la máquina. Los datos tenían que tener algún sentido y si las tarjetas perforadas no desvelaban su misterio, escribiría otras nuevas. Ya había obtenido permiso para escribir sus propios programas de aprendizaje; de hecho se lo había recomendado. Sin embargo sus planes tuvieron que esperar cuando se encontró al teniente, vuelto de uno de sus turnos en la torre de vigía.</p>

<p class="d">Buenos días, Imi, ¿ha habido algún problema?</p>

<p class="d">Arti está enfermo.</p>

<p class="d">Sí, pobre chico, ya me han comentado. Tuvieron una buena aventura ayer.</p>

<p class="d">Sí, pero…</p>

<p class="d">Ya, ya, lo entiendo. Bueno, lo que quería decir es si había pasado algo aquí. Veo que has estado trabajando con los test de diagnóstico.</p>

<p class="d">Sí, ví unos datos meteorológicos muy raros. Los filtré y decían desconocidos y también decían que estaban bien, pero eran imposibles con los otros datos a la vez. No podían ser.</p>

<p class="d">Ya, no le des tanta importancia. Serán los instrumentos de medida; también se estropean, ¿vale?</p>

<p class="d">Sí, pero también los repasé y están bien. Y no creo que se estropeen y se reparen solos.</p>

<p class="d">Bueno, no, eso es verdad. Sin embargo, a veces algo con lo que no contamos, el sabotaje de una gaviota, por ejemplo, hace disparatar los resultados.</p>

<p>Imi no parecía muy convencido. &mdash;Para que eso pasara tendría que haber pasado en varios instrumentos a la vez. Y uno de ellos está en la torre, otro aquí. No parece muy plausible.</p>

<p class="d">Bueno, ¿y qué puedes hacer?</p>

<p class="d">He estado repasando el manual y he visto un algoritmo de análisis de datos que creo que podría implementar y luego, bueno, se lo enseño al Doctor Zenón, él es físico. A lo mejor sabe lo que es esta cosa rara. ¿Le parece mal?</p>

<p class="d">No, lo que me parece es que te va a ser muy complicado. Vas a cansar esa cabeza que tienes, ¿estás seguro que quieres hacerlo?</p>

<p class="d">Sí.</p>

<p class="d">Pues adelante; yo estaré con otra tarea tan importante como excitante: redactar el inventario, pero si necesitas ayuda, estaré en mi despacho.</p>

<hr>

<p>Imi aprovechó cada momento que pudo escurrir. Su único descanso fue el almuerzo y la única desviación el turno que pasó acompañando a Arti, tan benditamente adormilado como antes. El resto del tiempo lo hubieras sorprendido sentado en el suelo del sótano, las piernas cruzadas, el manual extendido y dos cuadernos llenos de notas y garabatos. Tras el almuerzo tenía casi imaginado en la mente el programa que iba a engendrar, pero los cálculos de recursos le daban la espalda: la máquina de boole tenía insuficiente memoria para que su programa pudiera funcionar.</p>

<p>Rechazó una primera solución de hacer imprimir los resultados parciales usando las tarjetas perforadas como una especie de memoria extra para al final empaquetar todos los datos en un simple vector de datos exclusivamente numéricos. Tuvo que diseñar al mínimo detalle la estructura, hasta el punto que al punto de llegar esa noche no había escrito ni una de las tarjetas perforadas. Todo aún estaba a medias en su cabeza y otra media en apuntes emborronados cuando tuvo que soportar las amistosas burlas del teniente.</p>

<p class="d">¿Qué, se ha cansado esa cabeza?</p>

<p class="d">Da igual, todavía no he terminado.</p>

<p>Imi continuó trabajando en el algoritmo en cada momento que tuvo libre durante tres largas semanas. Arti se iba recuperando muy poco a poco, abandonando la cama en favor del sillón y de lentos paseos a mediodía alrededor de la estación. El trabajo de Imi siguió un curso muy parecido, con más sudor que gloria, sin grandes éxitos hasta que al final pudo entregar al doctor Zenón un listado de datos e irse a dormir.</p>

<p>A la mañana siguiente, encontró sobre su listado una breve nota del doctor: <q>Unos números interesantes para la física teórica. Pero son imposibles. Si no, se habría roto el espacio y el tiempo y vuelto a remendar. Y eso es imposible; ¿universos múltiple? Bah, no puede ser. La explicación más sencilla es que hay algún error. Repasar cálculos.</q></p>


