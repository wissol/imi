<h3>El Templo de los Tiempos Remotos</h3>

<p>Transcurría una jornada excepcionalmente seca en la Estación; esto es no llovió en absoluto. El Doctor Tad, el doctor Zenón y Palwyr habían pasado los días y los meses excavando llenos de tanta esperanza como paciencia. El joven se había mostrado especialmente entusiasta, reclutando a los aprendices, a las cocineras, a Arti e incluso al teniente, si se le presentaba la ocasión. De haberse tropezado contigo en la playa, querido lector, seguramente también te habría pedido ayuda. Su padre se mostraba más realista: de encontrar aquí un templo de dimensiones catedralicias &mdash;que era lo que su hijo tenía en mente&mdash; o cualquier otro complejo urbano, su exploración requeriría de un equipo de decenas de personas. Además era muy poco probable que en esta isla una civilización calcolítica o incluso de la era del bronce pudiera reunir los recursos y la gente necesaria para levantar algo así. Lo que él imaginaba era algo más parecido a los más primitivos templos paganos; quizás poco más que una choza donde resguardar ídolos y ofrendas, o un simple altar de piedra para los sacrificios.</p>

<p>Lo que nadie esperaba fue encontrar un pasadizo. La primera impresión que se llevó Palwyr fue de una especie de marco de piedras relleno de tierra. Las dimensiones eran las de una ventana pequeña, ¿pero qué hace una ventana en el suelo? Excavó con sus propias manos, hasta descubrir una loseta. Excitado, removió toda la tierra que la cubría: allí estaba, en su centro, un bajorelieve circular que debía encajar perfectamente con el aro que habían descubierto. Palwyr corrió a buscar a su padre con la carra cubierta de polvo y de alegría.</p>

<hr>

<p class="d">Corran, corran &mdash;Palwyr animaba a los científicos a seguirle a la excavación.</p>
<p class="d">¿Estás seguro de lo que viste? &mdash;El doctor Zenón preguntaba como excusa para recuperar resuello.</p>
<p class="d">Que sí, &mdash;Palwyr se detuvo disgustado&mdash; ni que les hubiera dicho que he visto a un dragón.</p>
<p class="d">Bueno, si no es un dragón, &mdash;replicó su padre&mdash; tampoco va a salir volando, ¿verdad? Podemos ir andando como corresponde a un caballero.</p>
<p>Palwyr enrojeció; desde pequeño todos sus familiares le recordaban en cuanto tenía ocasión que, como <q>vivía en los límites de la civilización</q>, sus modales podían caer más bajo que los de un caballero. En realidad le importaba lo justo y necesario, pero la puya de su padre la sintió como una traición. &mdash;Padre, ¡puede ser el descubrimiento de nuestras vidas!</p>
<p class="d">Ah, el éxito, tan grato es a los jóvenes, como vano el aplauso del pueblo. &mdash;El doctor Zenón parecía que declamar mientras sus dos compañeros lo miraban boquiabiertos. &mdash;Todavía guardo mi medalla de ingreso como Académico de las Ciencias, por alguna parte supongo. </p>
<p class="d">Bueno, pues anden, por favor.</p>

