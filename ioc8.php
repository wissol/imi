<p class="amano">Drugtri, usurpador, ¿dónde están los príncipes?, ¿puedes contestarnos a esto?</p> 

<p class="amano">Drugtri, usurpador, ¿quién asesinó a sus padres?, ¿puedes contestarnos a esto?</p>

<p class="amano">Conde de Valgravia, escucha al valor de tu propia arma. Sabemos que sabes que Drugtri es un usurpador y un asesino. Que el miedo no detenga tu espada vengadora. Si los niños príncipes aún están vivos tú eres el único que puedes salvarlos. Conde de Valgravia, ¿es noble la sangre que corre por tus venas? ¿Acaso temes a la muerte? Yo también y aquí estoy.</p>
