<h2>Capítulo 8 Un Pergamino en la Puerta</h2>

<p>En alguna tierra remota empezó la rebelión contra Drugtri, o mejor dicho, la chispa prendió en muchas aldeas de la frontera, alimentándose con injusticias y abusos, escondiéndose en todas las oscuridades hasta que, por su propia luz, resplandeció como un faro. Nació en canciones, en chistes y burlas de campesinos; creció entre los comerciantes hartos de registros e impuestos y fue escalando hasta llegar al Conde de Valgravia.</p>

<p>Desde alguna tierra remota bajó una figura enfundada en la sotana negra de los filósofos. Caminó en medio de la noche, con tanto vigor como le permitían sus viejos músculos, sin más arma que un pergamino. Llegó a Valgravia con la salida del sol y esperó sentado con los demás viajeros y comerciantes a que abrieran las puertas de la ciudad. Caminó con la parsimonia de los académicos hasta llegar a la puerta del mercado. Allí, ante la muchedumbre que se congregaba para iniciar el comercio, entre anuncios de especias y ofertas de trabajo, clavó su pergamino.</p>

