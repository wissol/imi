<p>Dos minutos después de que su hijo desapareciera de la vista, el Doctor Tad se palmó la cartera. &mdash;Oh, por Çatal Hüyük, ¡mirad dónde estaba el cuaderno todo el tiempo! Arti, desgraciadamente yo soy demasiado mayor para entrar ahí y necesito a un joven valiente que explore el túnel, ¿querrías ser tú?</p>

<p class="d">Señor, sí, ¿habrán tesoros?</p>

<p class="d">Pues claro que sí, pero no toques nada, solo quiero que entres y me describas después lo que veas.</p>

<p class="d">Doctor Tad, ¿podemos acompañarle? &mdash;preguntó Zaq.</p>

<p>El doctor hizo ademán de reflexión durante un minuto. &mdash;Está bien, sí buena idea, seguro que Arti encontrará útil la ayuda de dos jóvenes inteligentes como vosotros. Zaq, como eres el de mayor graduación tomarás el mando, ¿de acuerdo?</p>

<h3>Más Allá</h3>

<p>El filósofo estaba sentado en la cama de una habitación del palacio del Conde de Valgravia. Oficialmente estaba en calidad de invitado, pero un guardia escoltaba la puerta <q>por su seguridad</q>, hasta la mañana siguiente cuando su anfitrión le comunicaría si encabezaba la revuelta o por el contrario se mantenía fiel a Drugtri con todas las terribles consecuencias.</p>

<p>Lentamente, con la pereza del sol en una mañana de invierno, fue acariciando sus oídos una extraña música. ¿Estarían preparando una fiesta en el palacio? ¿Sería eso malo o bueno? La parte más racional del filósofo le decía que no era apropiado comenzar una revuelta dando una fiesta, pero quizás el conde necesitara de vino y placeres para aplacar su miedo.</p>

<p class="d">Disculpe la intromisión mía.</p>

<p>El filósofo se volvió sobresaltado, para descubrir a una especie de enano de ojos saltones, con una decena de extraños bichos cuadrados, como sellos con patas que le recorrían el cuerpo.</p>

<p class="d">¿Cómo ha entrado usted aquí?</p>

<p class="d">Gracias a su miedo. Años hace que con usted hablar quería, de corazón. Pero usted muy mal ensueña, sin ofender, sin atacar. Amdiufyniud mi nombre es y buenas noticias traigo.</p>

<p class="d">¿Buenas noticias? ¿Pero qué es usted? ¿Y qué significa eso del miedo y del ensueño? ¿Estoy borracho? &mdash;El filósofo luchaba por hacer desaparecer de sus ojos lo que su mente no podía aceptar.</p>

<p class="d">Las buenas noticias que los príncipes viven, sí. En un lugar muy lejano escondidos viven sí. Pero quizás Drugtri alcanzarlos pueda. Mi corazón y mi alma teme eso. De lo demás le puedo decir…</p>

<p>Amdiufyniud siguió hablando pero sin emitir sonido, brilló levemente un segundo y se desvaneció lentamente en la penunmbra de la habitación dejando solo uno de sus consejeros que fue caminando hasta escalar la mesa.</p>

<p class="d"><q>Mi aliento no es a vino, ¿sueño? Quizás sí, porque son noticias que me gustan. ¿o soy uno de esos pobres alienados?</q></p> 

<h3>Que Renazca lo Antiguo</h3>

<p>Arti entró el primero  &mdash;por una razón tan peregrina como haber sido el primero en ser nombrado&mdash; orgulloso de portar la primera linterna que iluminaba una era perdida y nuevamente revelada. El asombro de Arti era tan grande como el de cualquier otro niño que se hubiera enfrentado a una ocasión así. Sí, había viajado ya a un mundo desconocido y pasado experiencias trágicas; pero las sombras le recordaban a los ritos proscristos y arcanos de la Madre de la Sombra y las extrañas figuras que su imaginación forjaba con los reflejos de su linterna le ponían en alerta. ¿Qué sería aquello? ¿Qué trampa ocultaba? ¿Qué monstruo podía estar acechando su garganta? El aire cargado de polvo de los siglos entraba en su nariz con un olor intenso a cera de abeja, a grasa y a una amalgama de tonos que no podía identificar. Olía, en suma, a desconocido, a una frontera que no había transpasado nunca y por un momento tembló de miedo.</p>

<p>Su cabeza tropezó con el techo, obligándole a avanzar a gatas. El túnel se sentía ápero y polvoriento en sus manos, y en para sus rodillas. La conversaciones de la superficie habían quedado atrás y solo el ruido de sus compañeros y sus ocasionales preguntas le reconfortaban.</p>

<p class="d">Arti, ¿es muy grande?</p>

<p class="d">No lo sé. No se ve el fin.</p>

<p>Tosió, el polvo le picaba en la garganta. El miedo iba infiltrándose en sus pensamientos y cada vez se le hacía más difícil controlarlo. Se encontraba recordando sus días en la torre y una parte de él quería llorar, volverse y llamar a mamá; pero ahora no tenía a mamá, ni tan siquiera a su hermano mayor. Inspiró.</p>

<p class="d">Eh, Arti, ¿te mueves?</p>

<p class="d">Un momento. &mdash;Cerró los ojos y recordó unas palabras de Ceslas. <q>Arti, tienes miedo, vale, ya sabemos que esto es peligroso y ya sabes que tienes que tener cuidado. Por lo demás el miedo no sirve para nada. ¡Piensa, Arti, piensa! y no sientas nada</q>. Y Arti pensó que al fin de cuentas solo tenía que tener cuidado donde pisaba.</p>

<p>Siguió adelante. Volvió a toser otra vez, <q>es el polvo</q>. Por fin descubrió la primera pista de que esto era algo más que un largo tunel sin sentido: un texto largo escrito es unos glifos que no podía leer pero que ya había visto en las reliquias que Palwyr le había mostrado en uno de sus recreos. Sonrío, sí, él, Arti, aunque no fuera un príncipe iba a ser alguien importante, el criado que descubrió un tesoro. Unos pasos más y se topó con una puerta de madera remachada en bronce.</p>

<p class="d">¡Hay una puerta!</p>

<p class="d">¿Está cerrada?</p>

<p class="d">Mira a ver si se abre</p>

<p>Arti la empujó con la mano izquierda; al tocarla el maderamen podrido se derrumbó; levantando una nube de polvo y escombros. Tosió.</p>

<p class="d">Arti, ¿estás bien?</p>

<p class="d">¿No estás herido?</p>

<p class="d">No, bueno, tengo un poco de sangre en la mano, pero es <q>una raspadadura</q>.</p>

<p class="d">¿Estás seguro?</p>

<p class="d">Sí. Voy a entrar. Quito esta basura y entro.</p>

<p class="d">Ten cuidado.</p>

<p class="d">Vale.</p>

<p>Apartó los escombros hasta abrir una oquedad donde poder pasar; se arrastró y alzó los ojos, para descubrir lo más maravilloso que sus ojos de príncipe o de criado habían visto nunca: una pared forrada en pan de oro, mesas de plata, vasijas, un arcón incrustrado de piedras preciosas y marfil de mamut.</p>

<p class="d">¡Es un tesoro! ¡Uno de verdad! ¡Venid a ver! ¡Somos ricos! ¡Todos ricos somos!</p>
