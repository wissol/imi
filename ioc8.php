<p>Cuando llegaron el padre de Palwyr no pudo sino dejar escapar una exclamación &mdash;Vaya.</p>
<p class="d">¿Lo ves? Padre, Doctor Zenón, ¿lo ven?</p>
<p class="d">Tendremos que abrirlo, supongo. &mdash;El Doctor Zenon miró al doctor Tad en busca de un gesto de aprobación.</p>
<p class="d">Supone usted bien. Tendremos que abrirlo, en efecto, pero preservando la piedra.</p>
<p class="d">Por eso he traído el aro. &mdash;anunció Palwyr alegremente.</p>
<p class="d">Hmm, creo que ese plan tropezará con alguna dificultad, Palwyr. Tanto la piedra como el aro están demasiado desgastados. No, será mejor pasar una cuerda y formar con ella un arnés. Así la levantaremos, ¿no está de acuerdo, Doctor Zenon?</p>
<p class="d">Oh sí, siempre que no sea muy pesada.</p>
<p class="d">Tiene usted razón Doctor Zenón. Palwyr, corre a buscar a Arti, el criado y a los aprendices. Necesitamos todos los brazos que podamos conseguir.</p>
<p class="d">Padre, ¿llamó también al teniente y el comodoro?</p>
<p class="d">No, &mdash;el Doctor Tad se rascó la nariz.&mdash; el otro día me dijeron que estaban cansándose de tus, ejem, invitaciones. Lo siento.</p>
<p class="d">Bueno, ellos se lo pierden</p>

<p>Tan pronto como Palwyr se alejó más allá del alcance de su oído, el doctor Zenón se encaró con su colega.</p>
<p class="d">Su mirada tiene razón, le he mentido a mi hijo.</p>
<p class="d">Disculpe, es asunto suyo.</p>
<p class="d">No se disculpe, la explicación es bien sencilla. Verá, si no estoy confundido esa loseta está diseñada para ser abierta; no debería ser imposible manejarla entre los que estamos. Lo que necesito son niños que se introduzca en la apertura y exploren el interior.</p>
<p class="d">Bueno su hijo cabe perfectamente.</p>
<p class="d">Querido colega, ¿sabe por qué muere un arqueólogo? Puede ser una piedra desprendida, o la caída en una sima; pero lo más peligroso son las bacterias y miasmas de enfermedades desconocidoas que llevan encerradas siglos. No, si miento a mi hijo es por su bien. Arti es buen chico, lo hará bien y el pobre es huerfáno. De todas maneras no será más que un criado toda su vida, ¿no es así?</p>
<p class="d">Si es que no muere antes de cualquier cosa. Los niños de esa clase mueren como pajaritos.</p>

<h3>Mi Príncipe de Manos Suaves</h3>

<p>Arti, sentado en el alféizar de una ventana de la despensa, acababa de terminar de pelar una cesta de cebollas y se puso a canturrear.</p>

<p class="d"><q>…porque los nerjios nunca beben, nunca beben…</q></p>

<p class="d">¡Oh!, ¿estás feliz? &mdash;Era Amy la pelirroja de trece años, recién llegada con otra cesta igual. &mdash;Pues aquí tienes más alegría</p>

<p class="d"><q>…nunca beben sin cantar</q>. Sí, muchas gracias muchas.</p>

<p>Amy y Arti se intercambiaron las cestas, rozándose sus manos brevemente. &mdash;Eh, tienes manos suaves, manos de príncipe, ¿te lo han dicho alguna vez?</p>

<p>Los pies descalzos de Arti se frotaron uno al otro como si tuvieran vida propia. &mdash;¿Sabes? Tuve un sueño que era un <q>poríncipe</q>.</p>

<p class="d">Príncipe, se dice príncipe. ¿Y qué pasó?</p>

<p class="d">Bueno éramos <q>pori-príncipes</q>, yo y hermano mí… eh… mi hermano y yo y teníamos un tío que era muy bandido y quería ser rey y que murieron nuestros padres y entonces nuestro tío tenía que cuidarnos pero nos dice mentira y nos encierra en una torre y estaba muy oscuro y hacía frío y poca comida.</p>

<p class="d">¿Y había ratas?</p>

<p class="d">No solo ratones y cucarachas y mosquitos y pulgas y creo que una babosa pero hermano mí, mi hermano no la vió. Y nuestro tío quería que muriéramos porque sería rey, pero no nos <q>moriríamos</q>, así que mandó que nos mataran, pero vino Imi hizo magia y nos escapó a aquí.</p>

<p class="d">Oh, qué historia más triste</p>

<p class="d"><q>Trieste</q> antes, en la torre. Daba mucho miedo y mucho lloraba. Aquí no es triste. Aquí no hay miedo y tengo amigos y tú también. Si vuelvo a ser príncipe te invito a mi reino y no tendrás que llorar por cebollas. Te lo <q>prometeo</q>.</p>

<p>Amy pensó que era el niño más mono del mundo. &mdash;Tú ya eres mi pequeño príncipe de manos suaves.</p>

<p>En ese instante Imi entró en la despensa. &mdash;Eh Arti, que dice Palwyr, que dice el Doctor Tad que vengas a la excavación con nosotros, que hay trabajo, y ponte algo en los pies, que afuera hace frío, ¿vale?</p>

<h3>Casi Muerto</h3>

<p>El brujo solo pensaba en el miedo. Todo su esfuerzo era inútil; la corriente lo arrastraba a la costa, pero no lo suficientemente rápido. Las fuerzas necesarias para mantenerse a flote le estaban menguando: ya dos veces había tragado agua y una vez las olas lo habían sumergido dos metros para la superficie. A ese ritmo se ahogaría a diez metros de la costa; si es que las orcas, para el brujo monstruos marinos, no acababan antes con sus sufrimientos.</p>

<p>Entonces sus pies tocaron algo sólido y rodó por el suelo, exámine. Agitó la cabeza azorado, ¿esa oscuridad era la muerte? No, había vuelto a su cueva, la cueva de la Señora de la Sombra y el único rastro de su aventura era el agua de mar que chorreaba su capa.</p>

<p>Lo había conseguido; había ido y vuelto. Una parte de él quería correr a congraciarse con su señor Drugtri; pero le ganó la prudencia. &mdash;<q>No le diré nada</q>&mdash;se dijo&mdash; hasta que sepa cómo lo he hecho.</p>

<h3>El Agujero Oscuro y Terrible</h3>

<p class="d">Venga, vamos, todos a una, y despacio, no queremos romperlo.</p>

<p>El doctor Tad había atado la soga a la oquedad donde iría ajustada el aro, y se frotaba la nariz de los nervios, guiando la operación. Imi, Arti, Zaq y Palwyr, en ese orden, tiraban de la cuerda mientras el Doctor Zenón se tapaba la nariz con un pañuelo empapado de colonia. La primera tensión consiguió remover el barro que se había colado por las uniones. El silencio quebró ante el roce de la piedra contra el ladrillo y el rebotar de pequeños fragmentos. Un esfuerzo más y algo muerto pareció respirar por primera vez en eones.</p>

<p class="d">Apartáos, no os acerquéis, ese aire podría estar contaminado. Hay que dejar a la tierra respirar. Palwyr, por favor, ¿podrías traer mis notas? El cuaderno 35A, lo tengo en mi despacho, creo, o si no en el salón, o en el arcón.</p>

<p class="d">Padre.</p>

<p class="d">Lo sé, lo siento. Date prisa, lo necesito ahora. Pero tranquilo el yacimiento ya es tuyo en la historia, y no se va a mover. Vamos, corre.</p>

