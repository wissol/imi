		<p class="d">¿Sobre esta isla remota? ¿Con niños que hablan un idioma que nadie puede reconocer? ¿Sin restos? Bueno, tendremos que esperar a conocer la verdad. No es que diga que mientas, Imi, no es eso, pero tampoco tú sabes como llegaron a la isla en realidad.</p>

	<h3>¡Mentiste!</h3>

			<p>Ese día Zaq estuvo muy distante de Imi. Disimulaba mal su enfado, evitando hablar, y si demostraba algún otro sentimiento era el de miedo. En el brazo derecho, oculto bajo la manga, se había escrito una oración de protección contra el mal; no se separó en ningún momento del Libro de Oración Común y a cada oportunidad que tenía le daba un besito. Escondió dos dientes de ajo sobre la puerta de la habitación y trenzó una corona de flores de manzanilla bajo el colchón de su cama e hizo todo lo que pudo para evitar que Imi le tocara a él o a sus cosas. Pero al llegar la noche ya no pudo contener por más tiempo su rabia.</p>

			<p class="d">¡Mentiste!</p>

			<p>Imi le miró como quien mira a un loco <p class="d">¿Y qué querías que dijera?</p>

			<p class="d">Nada. Nada de nada. ¿Lo entiendes? Nada.</p>

