		<p class="d">Bueno, pues casi me perdí. No sé muy bien donde estaba, había una especie de bulto, o algo por ahí, allá fuera y eso y luego pues "me caminé" por la izquierda, lejos y los descubrí. Tenían miedo y frío y no les entendía nada, así que me los llevé a casa. Entonces pasamos por el salón, porque pensé que estaría calentito y eso y estaba ahí Zaq y se despertó y se puso tan nervioso que ya pues escuchamos un ruido y nos fuimos a la habitación. Además era de noche y todos queríamos dormir, los príncipes también.</p>

		<p class="d">¿Los príncipes?</p>

		<p class="d">Bueno, Zaq había soñado con príncipes, me dijo luego y los hemos llamado así, supongo.</p>

