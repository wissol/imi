	<h3>Encuentran los príncipes</h3>


		<p>Imi cerró el libro. No entendía nada, pero si ya estaban muertos, ¿cómo podría salvarlos? ¿Se suponía que tenía que ensoñar a cadáveres o a zombis? ¿Y para qué? De todas maneras nunca había podido ensoñar a nadie ni a nada a su capricho, siempre se colaba algo con lo que no contaba. ¿Cómo iba ahora a asegurar ensoñar precisamente a los príncipes y no a, pongamos por caso, a sus asesinos? ¿Y todo el riesgo, para qué? ¿Para darles una sepultura cristiana y digna?</p>

		<p>Aquel día Imi no pudo sacarse la idea de la cabeza, provocando hasta la impaciencia del Teniente Alistair al ver que no se concentraba en algo tan importante como <q>la reserva de datos y su empleo en la resolución de problemas lógicos</q>, básico para entender el funcionamiento del lenguaje de las máquinas. No pensaba en los príncipes, no pudo hacerlo ni en un solo momento hasta la noche, lo que distraía su mente es que como los príncipes ya estaban muertos, todo estaba perdido. Pasara lo que pasara no iba a ensoñar fantasmas de asesinados; su abuela le había advertido del peligro que eso suponía.  Pero tras la llegada de la noche, apagadas las luces, con la estación en pleno silencio, fue a buscar su libro y ya no estaba.</p>

		<p class="d">Zaq, Zaq, despiértate... pero no había nadie en su cama; nadie salvo uno de los pequeños sellos con patas que Amdiufyniud había llamado sus consejeros. Tampoco en esta ocasión pudo Imi examinarlo de cerca, porque al ser descubierto el bicho se escurrió hasta el suelo y desde allí, tan rápido como pudo, corrió hasta salir por debajo de la puerta.</p>

		<p>El viento frío enfríaba la ventana sobre la que Imi había apoyado la espalda, pero no más. El chico, sin buscar más abrigo que su pijama, salió de la habitación y exploró el pasillo. Allí le esperaba el consejero y al fondo la cálida luz que se difuminaba desde la sala donde el doctor Zenón había cantado <q>Monsieur Dupont</q>. Ahora los sonidos eran la respiración de las estufas y apenas un susurro más que recordaba al jadeo asustado de un niño. El consejero se dirigió hacia la luz e Imi lo siguió, al principio como quien teme acercarse a una trampa, pero después casi corriendo, sus pies hollando la vieja alfombra, hasta que adelantó a su guía y sin darse tiempo para dudar, entró en el salón.</p>

		<p>Sobre uno de los sofás reposaba el libro, su libro, abierto en el regazo de Zaq, que dormía profundamente. En el centro, la <em>puerta</em> que habían ensoñado juntos, rielaba como una luna ambarina, flotando a dos pies del suelo. Y en la esquina más distante, descalzos, cubiertos por sucios y deshilachados camisones con puños de encaje, se abrazaban juntos dos niños de nueve y once años.</p>

		<p class="d">Zaq, ahora sí, por favor, despiértate.</p>

		<p>Zaq se sobresaltó, sin llegar a avivarse completamente.</p>

		<p class="d">Hola, que no estáis muertos, ¿verdad?</p>

		<p>Los niños no contestaron más que como una mirada huidiza, alimentando los nervios de Imi.</p>

		<p class="d">¡Eh! ¿Quiénes son esos? ¿Son?... ¿Son?... Mi madre, ¿son?</p>

		<p>Imi asintió a las palabras de su amigo.</p>

		<p class="d">¿Crees que están?</p>

		<p class="d">¿Muertos?</p>

		<p class="d">O algo.</p>

		<p class="d">¿Tenemos qué tener miedo?</p>

		<p class="d">No sé.</p>

		<p class="d">Bueno, ¿hacemos algo?</p>

		<p class="d">¿Cómo cuándo lo de la navaja?</p>

		<p class="d">Vale.</p>

		<p>Imi dió un paso hacia los niños, provocando su miedo. Entonces decidió sentarse, de piernas cruzadas, sobre la alfombra. Nadie dijo nada durante mil momentos. Por fin, Zaq, se puso a cantar en voz queda &mdash;Monsieur Dupont, dididibi…</p>

		<p>Y entoces ocurrió algo mágico: del más pequeño de los niños, por primera vez en demasiado tiempo, brotó una risa de manantial.</p>

		<p class="d">Hola, &mdash;saludó Imi.</p>

		<p>El pequeño calló; el grande hizo ademán de levantar el puño, que transformó en un saludo cuando acabó por imitar a Zaq, quien movía exageradamente la mano de un lado a otro. Y por fin, una palabra.</p>

		<p class="d">Ho-o-la-a? &mdash;dijo el pequeño.

		<p class="d">Hola, &mdash;respondió Zaq.

		<p class="d">Imi, &mdash;dijo señalándose a sí mismo y luego dirigió su dedo a Zaq &mdash;Zaq.</p>

		<p class="d">Art, <p class="d">respondió el pequeño y señalando a su hermano, dijo &mdash;Ceslas.</p>

		<p class="d">¡Son los príncipes! &mdash;exclamó Zaq, rindiéndose por fin a sus ojos.

		<p class="d">O nuestra imaginación.</p>

		<p class="d">Kiné bez cuoz?</p>

		<p>Eso fue lo que preguntó Art, y escucharon todos, pero Imi entendió perfectamente: "¿Quiénes sois?" En ese momento, se dio cuenta de que el consejero estaba escalando sus pies.</p>

		<p class="d">Ya Imi zu e yi Zaq chí &mdash;Aunque esas fueron sus palabras, lo que Imi pensó en <q>Yo soy Imi y él es Zaq</q>.</p>

		<p class="d">¿Eté? (¿Dónde?)</p>

		<p>En ese momento escucharon el chirriar nocturno de una puerta y los cuatro se llenaron de miedo: los dos príncipes del terror a lo desconocido, los aprendices de cómo iban a explicar la presencia de sus invitados. En segundos, sin más explicaciones que los gestos, los cuatro se escabulleron a su habitación.</p>

