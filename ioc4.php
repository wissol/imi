			<h3>Vistiendo a los náufragos</h3>

			<p>Yenny, la cocinera se volvió loca buscando con qué vestir a los <em>naúfragos</em>. Desde luego no podían ir andando así en camisón todo el día y menos en una isla tan fría, <q>solo Dios sabe cómo no se han congelado los preciositos</q>. Sin pedir más permiso que un <q>por supuesto no te importará prestarme algo para los naúfragos</q>, recolectó ropas de los tres muchachos de la isla. Luego todo fue cuestión de medir, cortar, hilvanar, probar y coser; hasta que consiguió que ambos tuvieran una apariencia presentable. Solamente en sus miradas les quedaba algo extraño, pero Ezy, la hija menor atribuyó al susto del naufragio. ¿Qué otra cosa podía ser?</p>

			<p>Restaba el tema de los zapatos; lo que a una mujer de menos recursos le hubiera parecido un problema insoluble en aquella isla, a Ezy le llevó tres días. Lo justo para buscar tiempo, convertir un anzuelo en una aguja gorda y <em>apañar</em> pedazos de cueros de varios muebles que <q>no lo necesitaban</q> y retales de tela que almidonó y tiñó de oscuro hasta tener algo para la pala; esto es la parte superior del calzado. El resultado era indigno de cualquier zapatería de aficionados, pero suficiente para que los niños pudieran salir de casa sin la seguridad de congelarse.</p>

