	<h3>Alojando a los príncipes</h3>

		<p>El amanecer fue complicado. Imi y Zaq despertaron en una misma cama; Art y Ceslas seguían durmiendo en la suya.</p>

		<p class="d">¿Zaq, qué hacemos? ¿Los escondemos?</p>

		<p class="d">¿Por qué? No los hemos robado ni nada.</p>

		<p class="d">Ya…, ¿pero qué decimos?</p>

		<p class="d">Que los rescatamos… un naufragio… o se cayeron de algún dirigible o…</p>

		<p class="d">¿Y si decimos que nos los encontramos y no sabemos nada más.</p>

		<p class="d">Bueno… es la verdad, casi.</p>


		<h3>Explicaciones</h3>

		<p>Los demás miembros de la estación metereológica Príncipe Alfredo no tenían ni por dónde a empezar a buscar explicaciones. Las cocineras reaccionaron envolviendo a los recién llegados en mantas y atiborrándolos en el desayuno. Los de mente científica buscando infructuosamente en sus libros en qué idioma podrían encajar sus palabras. Los jefes, siendo jefes, se centraron en tratar de sonsacar a los aprendices una verdad, que de obtenerla, jamás creeirían.</p>

		<p class="d">¿Cómo que los encontrastéis?</p>

		<p class="d">Pues sí, ya estaban aquí, tendrían frío, supongo.</p>

		<p class="d">Zaq, ¿tú qué dices?</p>

		<p class="d">¿Yo?</p>

		<p class="d">Sí.</p>

		<p class="d">Bueno, yo, me paso una cosa muy rara, estaba soñando con ellos y los ví, aquí en el salón.</p>

		<p class="d">¿Es eso cierto, Imi?</p>

		<p class="d">Sí. Yo había salido fuera, porque estaba pensando en mi madre y me puse a caminar un poco, señor.</p>

		<p class="d">¿De noche?</p>

