		<p>La noche siguiente, tras los estudios y el trabajo, Imi tuvo la primera ocasión de leer detenidamente el libro. El papel tenía un tacto entre el algodón y la lija y por sus sutiles imperfecciones muy bien parecía que había sido fabricado en otro mundo, a mano. Sus letras no eran en absoluto las deformidades ininteligibles que Imi había esperado sino grafías cuidadas, siguiendo los canónes tipográficos y hasta con caprichos artísticos. Cada página estaba iluminada con grabados coloreados en rojo y azul que ilustraban los personajes y las extraordinariascriaturas que el libro mencionaba.</p>

		<p>Casi parecía un cuento de hadas, por lo siniestro: versiones siempre en miniatura de unicornios, grifos, ogros, bumblebastics como Amdiufyniud, dragones como Seren y otros cuyos nombres les era completamente desconocidos. Ninguno vivía en ninguna parte sino que se manifestaban en los mundos <em>naturales</em> cuando cualquier persona de esos mundos <em>naturales</em> los convocaba. Imi estaba inseguro de saberque significaría aquello, pero suposo que sería algo parecido a sus ensueños. De todas maneras lo que más le interesaba era concentrarse en la historia de los príncipes.</p>

