<h3>El Primer Viaje del Brujo</h3>

<p>¿Cómo se sueña a placer? <q>Sé tú que soñar</q> decía el oráculo de los huesos, y por ahí empezó el brujo. Nunca había visto a los príncipes, ni siquiera en retrato, ni siquiera en las borrosas visiones tras los sacrificios. Pero había visto la isla de la estación Príncipe Alberto. Cerró los ojos, la memoria le trajo recuerdos de brisas, de mar y de chillidos de aves. Su imaginación dibujó el perfil de la isla sobre el horizonte; en su cara sintió el frescor del norte, poco a poco meditó hasta relajarse y después hasta dormirse profundamente.</p>

<p>Y no pasó nada.</p>

<p>Los intentos del brujo se espaciaron a lo largo de los meses. Con cada fracaso agradecía no haber contado nada a Drugtri, quien se había concentrado con mandar jinetes, cada vez con menos disimulo, a todo lo ancho del reino y aún del mundo, sin encontrar más que farsantes y falsos rumores. Todas las noches el ursupador se acostaba diciéndose que debía dejarlo estar, que en nada ya podían los príncipes hacerle daño, pero todas las mañanas, cubierto en sudores fríos la pesadilla de la horca hacía renacer su propósito.</p>

<p>El primer viaje del brujo sucedió sin pensar, sin buscarlo, sin meditar. Se despertó inmerso en el agua, lanzado como un juguete por las olas. Estaba muerto de miedo, no sabía nadar; ni siquiera había tocado el agua salada antes de ese día. Solo le salvó recordar lo que le había dicho un marino: cerró los ojos, cesó de respirar y dejó que el agua lo llevara hasta la superficie.</p>

<p>¡Y sí, ahí estaba, la isla de sus visiones! Era un mago verdadero, y ahora, a cientos de metros de la costa, se dejó arrastrar por la corriente. Su única oportunidad es que el mar se apiadara de él. Cerca, nadaban las orcas.</p>

