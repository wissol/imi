<h3>El Ensueño del Brujo</h3>

<p>Había caído la noche sobre la cueva de la Señora de las Sombras. El brujo, que ya no se fiaba de su protección, dormitaba afuera bajo estrellas distantes. El sueño le venía y volvía con facilidad, entreverado de pesadillas y terrores nocturnos. Toda su magia, excepto las visiones, se había revelado falsa; el conocimiento antiguo inútil, excepto precisamente el que le indicaba que sus predicciones eran erróneas. Drugtri lo mandaría ejecutar en cuanto se diera cuenta, quizás ya estuviera pagando al verdugo.</p>

<p>Senen, el pequeño dragón, le cagó encima.</p>

<p>El brujo se volvió, no podía creer lo que veían sus ojos. Áquel ser, no más grande que un halcón de eleonor, le miraba con ojos desafiantes. Si no fuera porque era imposible, casi diría que el animal le había dejado su regalo encima a propósito. Pero por otro lado aquella criatura era imposible. Por un momento su mente quiso clasificarlo como serpiente voladora o quizás un lagarto volador, pero ni serpientes ni lagarto escupían fuego.</p>

<p>Aterrado, el brujo se levantó y se lanzó a correr, con Senen a su espalda hasta que lo perdió de vista. Todavía era de noche, pero se juraba a sí mismo que no lo había soñado. ¿Estaría volviéndose loco? ¿Y si era así, por qué tenía restos de excrementos en su capa y quemada una de las tiras de su sandalia? No, todo su ser le decía que aquella criatura la había llamado él mismo de alguna manera y sonrío; quizás aún guardaran sus huesudas manos algo de poder; ahora solo era cuestión de aprender a emplearlos. Con renovadas energías volvió a la cueva y a los libros de huesos.</p>

